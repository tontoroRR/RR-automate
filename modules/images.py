class UnitConverter:
    __DICT: dict = {
            'images/': '',
            'unit' : '',
            'hero' : '',
            '.png' : '',
            'Max' : '(Max)',
            'HQ' : 'Harleyquin',
            '_ALT_' : '',
            }

    @staticmethod
    def convert_img_to_name(_name: str):
        name = _name
        for k, v in UnitConverter.__DICT.items():
            name = name.replace(k, v)
        return name
